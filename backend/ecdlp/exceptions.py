"""
Модуль исключений для ECDLP решателя.
Обеспечивает типизацию ошибок для обработки в основном приложении.
"""

class ECDLPError(Exception):
    """Базовое исключение для всех ошибок модуля ecdlp."""
    pass

class CalculationInterrupted(ECDLPError):
    """
    Выбрасывается, когда выполнение алгоритма было остановлено пользователем
    через CancellationToken (threading.Event).
    """
    pass

class SolverError(ECDLPError):
    """
    Выбрасывается, если алгоритм не может найти решение 
    (например, точка не принадлежит подгруппе).
    """
    pass

class InvalidCurveError(ECDLPError):
    """
    Выбрасывается при обнаружении некорректных параметров кривой 
    или несовместимости выбранного метода с типом кривой.
    """
    pass

class AuditFailedError(ECDLPError):
    """
    Выбрасывается, если предварительная проверка (аудит) кривой 
    показывает, что задача вычислительно невыполнима данными методами.
    """
    pass